package com.cifpay.lc.util;

import java.util.Collection;
import java.util.HashSet;
import java.util.Set;

public class CollectionUtil {

	public static Collection<String> distinctString(Collection<String> list) {

		Set<String> distinct = new HashSet<String>(list.size());
		for (String item : list) {
			distinct.add(item);
		}

		return distinct;
	}
}
