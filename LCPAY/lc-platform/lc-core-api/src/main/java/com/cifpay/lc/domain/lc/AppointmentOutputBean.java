package com.cifpay.lc.domain.lc;

import java.math.BigDecimal;

public class AppointmentOutputBean extends AbstractLcOutputBean {

    private static final long serialVersionUID = 7433159208758154930L;

    private long appointmentId;            // 履约ID
    private BigDecimal appointmentAmount;    // 履约金额

    public long getAppointmentId() {
        return appointmentId;
    }

    public void setAppointmentId(long appointmentId) {
        this.appointmentId = appointmentId;
    }

    public BigDecimal getAppointmentAmount() {
        return appointmentAmount;
    }

    public void setAppointmentAmount(BigDecimal appointmentAmount) {
        this.appointmentAmount = appointmentAmount;
    }

    @Override
    public String toString() {
        return "AppointmentOutputBean{" +
                "appointmentId=" + appointmentId +
                ", appointmentAmount=" + appointmentAmount +
                '}';
    }
}
