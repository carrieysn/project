package com.cifpay.lc.domain.batch;

import java.io.Serializable;
import java.math.BigDecimal;

public class BatchApplyOutputDto implements Serializable {

	private static final long serialVersionUID = 2619783296172742688L;

	private Long lcId; 			// 银信证ID
	private String lcStatus; 	// 银信证状态
	private String lcStatusDesc; // 银信证状态描述

	private Long lcAppointmentId; 	// 履约ID
	private Long lcConfirmId; 		// 申请解付ID
	private BigDecimal lcPayAmount; // 申请解付金额
	
	public Long getLcId() {
		return lcId;
	}
	public void setLcId(Long lcId) {
		this.lcId = lcId;
	}
	public String getLcStatus() {
		return lcStatus;
	}
	public void setLcStatus(String lcStatus) {
		this.lcStatus = lcStatus;
	}
	public String getLcStatusDesc() {
		return lcStatusDesc;
	}
	public void setLcStatusDesc(String lcStatusDesc) {
		this.lcStatusDesc = lcStatusDesc;
	}
	public Long getLcAppointmentId() {
		return lcAppointmentId;
	}
	public void setLcAppointmentId(Long lcAppointmentId) {
		this.lcAppointmentId = lcAppointmentId;
	}
	public Long getLcConfirmId() {
		return lcConfirmId;
	}
	public void setLcConfirmId(Long lcConfirmId) {
		this.lcConfirmId = lcConfirmId;
	}
	public BigDecimal getLcPayAmount() {
		return lcPayAmount;
	}
	public void setLcPayAmount(BigDecimal lcPayAmount) {
		this.lcPayAmount = lcPayAmount;
	}
	
}
