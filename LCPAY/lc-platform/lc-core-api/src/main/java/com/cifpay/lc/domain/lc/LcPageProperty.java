package com.cifpay.lc.domain.lc;

import java.io.Serializable;

public class LcPageProperty implements Serializable {

	private static final long serialVersionUID = -3006146739742770517L;

	Long lcId;
	String lcPageUri; // 银信证静态页面资源地址
	int isPage; // 是否生成静态页面，生成之后lcPageUri才不为空

	public Long getLcId() {
		return lcId;
	}

	public void setLcId(Long lcId) {
		this.lcId = lcId;
	}

	public String getLcPageUri() {
		return lcPageUri;
	}

	public void setLcPageUri(String lcPageUri) {
		this.lcPageUri = lcPageUri;
	}

	public int getIsPage() {
		return isPage;
	}

	public void setIsPage(int isPage) {
		this.isPage = isPage;
	}
}