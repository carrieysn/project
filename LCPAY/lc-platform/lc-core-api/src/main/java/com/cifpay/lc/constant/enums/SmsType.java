package com.cifpay.lc.constant.enums;

public enum SmsType {

	SMS_CP200(1, "银信证CP200短信模板"), 
	SMS_CP300(2, "银信证CP300短信模板"), 
	SMS_CP500(3, "银信证CP500短信模板"), 
	SMS_OPEN(4, "银信证开证短信"), 
	SMS_REFUND(5, "银信证退款短信模板");

	private int code;
	private String desc;

	private SmsType(int code, String desc) {
		this.code = code;
		this.desc = desc;
	}

	public int getCode() {
		return code;
	}

	public void setCode(int code) {
		this.code = code;
	}

	public String getDesc() {
		return desc;
	}

	public void setDesc(String desc) {
		this.desc = desc;
	}

	
}
