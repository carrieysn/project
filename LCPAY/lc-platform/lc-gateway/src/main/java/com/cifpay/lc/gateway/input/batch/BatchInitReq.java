package com.cifpay.lc.gateway.input.batch;

import java.util.List;

public class BatchInitReq {

	private String merId; // 商户号
	private List<InitLcInputDto> lcList; // 银信证参数
	private String returnUrlBatch; // 返回地址
	private String noticeUrlBatch; // 通知地址

	private String payerBankCode; // 付款方银行代码
	private String payerBankAccountNo; // 付款人账号
	private String payerAccountType; // 付款人类型：c:个人 b:企业
	private String payerMobile; // 付款人手机号

	private String remarkBatch;

	public String getMerId() {
		return merId;
	}

	public void setMerId(String merId) {
		this.merId = merId;
	}

	public List<InitLcInputDto> getLcList() {
		return lcList;
	}

	public void setLcList(List<InitLcInputDto> lcList) {
		this.lcList = lcList;
	}

	public String getPayerBankCode() {
		return payerBankCode;
	}

	public void setPayerBankCode(String payerBankCode) {
		this.payerBankCode = payerBankCode;
	}

	public String getPayerBankAccountNo() {
		return payerBankAccountNo;
	}

	public void setPayerBankAccountNo(String payerBankAccountNo) {
		this.payerBankAccountNo = payerBankAccountNo;
	}

	public String getPayerAccountType() {
		return payerAccountType;
	}

	public void setPayerAccountType(String payerAccountType) {
		this.payerAccountType = payerAccountType;
	}

	public String getReturnUrlBatch() {
		return returnUrlBatch;
	}

	public void setReturnUrlBatch(String returnUrlBatch) {
		this.returnUrlBatch = returnUrlBatch;
	}

	public String getRemarkBatch() {
		return remarkBatch;
	}

	public void setRemarkBatch(String remarkBatch) {
		this.remarkBatch = remarkBatch;
	}

	public String getNoticeUrlBatch() {
		return noticeUrlBatch;
	}

	public void setNoticeUrlBatch(String noticeUrlBatch) {
		this.noticeUrlBatch = noticeUrlBatch;
	}

	public String getPayerMobile() {
		return payerMobile;
	}

	public void setPayerMobile(String payerMobile) {
		this.payerMobile = payerMobile;
	}

}
