package com.cifpay.lc.gateway.output.lc;

public class InvalidateResp extends LcBaseResp {

	private String invalidateAmount;	// 失效金额
	private String invalidateId; 		// 失效流水Id

	public String getInvalidateAmount() {
		return invalidateAmount;
	}

	public void setInvalidateAmount(String invalidateAmount) {
		this.invalidateAmount = invalidateAmount;
	}

	public String getInvalidateId() {
		return invalidateId;
	}

	public void setInvalidateId(String invalidateId) {
		this.invalidateId = invalidateId;
	}

}
