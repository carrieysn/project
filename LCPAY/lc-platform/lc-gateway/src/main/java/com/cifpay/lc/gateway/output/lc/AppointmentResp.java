package com.cifpay.lc.gateway.output.lc;

/**
 * 履约返回参数
 */
public class AppointmentResp extends LcBaseResp {
	
	private String appointmentId; 		// 履约Id
	private String appointmentAmount; 	// 履约金额

	public String getAppointmentId() {
		return appointmentId;
	}

	public void setAppointmentId(String appointmentId) {
		this.appointmentId = appointmentId;
	}

	public String getAppointmentAmount() {
		return appointmentAmount;
	}

	public void setAppointmentAmount(String appointmentAmount) {
		this.appointmentAmount = appointmentAmount;
	}
}
