package com.cifpay.lc.gateway.output;

import java.util.Map;

public class NormalViewResponse extends NormalResponse {
	private String viewPage;
	private Map<String, ?> pageModel;

	public NormalViewResponse(String viewPage, Map<String, ?> pageModel) {
		this.viewPage = viewPage;
		this.pageModel = pageModel;
	}

	public String getViewPage() {
		return viewPage;
	}

	public void setViewPage(String viewPage) {
		this.viewPage = viewPage;
	}

	public Map<String, ?> getPageModel() {
		return pageModel;
	}

	public void setPageModel(Map<String, ?> pageModel) {
		this.pageModel = pageModel;
	}
}
