package com.cifpay.lc.gateway.input.lc;

/**
 * 开证请求参数
 *
 */
public class OpenReq {

	private long lcId;		// 银信证ID
	private String remark;	// 备注

	public long getLcId() {
		return lcId;
	}

	public void setLcId(long lcId) {
		this.lcId = lcId;
	}

	public String getRemark() {
		return remark;
	}

	public void setRemark(String remark) {
		this.remark = remark;
	}
}
