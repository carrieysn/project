package com.cifpay.lc.thirdtradeadapter.adapter.icbcbankent.xmltmpl;

/**
 * 
 *
 */
public enum XmlRequestTemplate {

	PAYENT("PAYENT", "PAYENT-Request-Tempalte.xml"),

	QPAYENT("QPAYENT", "QPAYENT-Request-Tempalte.xml");

	private String transCode;
	private String templateName;

	private XmlRequestTemplate(String transCode, String templateName) {
		this.transCode = transCode;
		this.templateName = templateName;
	}

	public String getTransCode() {
		return transCode;
	}

	public String getTemplateName() {
		return templateName;
	}
}
