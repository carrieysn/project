package com.cifpay.lc.bankadapter.unionpay.output;

public class RefundResult {
	
    private String txnType; //交易类型
    
	private String txnSubType;//交易子类型
	
	private String channelType;//渠道类型
	
	private String merId;//商户代码
	
	private String orderId;//商户订单号
	
	private String origOryId;//原始交易流水号
	
	private String txnTime;//订单发送时间
	
	private String txnAmt;//交易金额
	
	private String reqReserved;//请求方保留域	
	
	private String queryId;//交易查询流水号
	
	private String respCode;//应答码
	
	private String respMsg;//应答信息
	

	

	public String getTxnType() {
		return txnType;
	}

	public void setTxnType(String txnType) {
		this.txnType = txnType;
	}

	public String getTxnSubType() {
		return txnSubType;
	}

	public void setTxnSubType(String txnSubType) {
		this.txnSubType = txnSubType;
	}

	public String getChannelType() {
		return channelType;
	}

	public void setChannelType(String channelType) {
		this.channelType = channelType;
	}

	public String getMerId() {
		return merId;
	}

	public void setMerId(String merId) {
		this.merId = merId;
	}

	public String getOrderId() {
		return orderId;
	}

	public void setOrderId(String orderId) {
		this.orderId = orderId;
	}

	public String getOrigOryId() {
		return origOryId;
	}

	public void setOrigOryId(String origOryId) {
		this.origOryId = origOryId;
	}

	public String getTxnTime() {
		return txnTime;
	}

	public void setTxnTime(String txnTime) {
		this.txnTime = txnTime;
	}

	public String getTxnAmt() {
		return txnAmt;
	}

	public void setTxnAmt(String txnAmt) {
		this.txnAmt = txnAmt;
	}

	public String getReqReserved() {
		return reqReserved;
	}

	public void setReqReserved(String reqReserved) {
		this.reqReserved = reqReserved;
	}

	public String getQueryId() {
		return queryId;
	}

	public void setQueryId(String queryId) {
		this.queryId = queryId;
	}

	public String getRespCode() {
		return respCode;
	}

	public void setRespCode(String respCode) {
		this.respCode = respCode;
	}

	public String getRespMsg() {
		return respMsg;
	}

	public void setRespMsg(String respMsg) {
		this.respMsg = respMsg;
	}
	


}
