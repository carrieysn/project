package com.cifpay.lc.std.domain.enums;

/**
 * 异步通知类型
 */
public enum NotifyType {
    OPEN("OPEN"),
    TRANSFER("TRANSFER"),
    INVALIDATE("INVALIDATE"),
    REFUND("REFUND");

    private String code;

    private NotifyType(String code) {
        this.code = code;
    }

    public String getCode() {
        return code;
    }
}
