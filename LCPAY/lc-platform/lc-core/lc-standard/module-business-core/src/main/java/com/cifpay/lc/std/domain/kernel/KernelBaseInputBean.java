package com.cifpay.lc.std.domain.kernel;

import java.io.Serializable;

public abstract class KernelBaseInputBean implements Serializable {

	private static final long serialVersionUID = 3232471460026439795L;

	private Long lcId; 		// 银信证ID(*)
	
	private String merId; 	// 商户号

	public Long getLcId() {
		return lcId;
	}

	public void setLcId(Long lcId) {
		this.lcId = lcId;
	}

	public String getMerId() {
		return merId;
	}

	public void setMerId(String merId) {
		this.merId = merId;
	}

	
}
