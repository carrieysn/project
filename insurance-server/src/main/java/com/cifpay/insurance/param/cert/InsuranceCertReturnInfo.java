package com.cifpay.insurance.param.cert;

import java.io.Serializable;

/**
 * 保险证申请退货
 * 
 * @author 张均锋
 *
 */
public class InsuranceCertReturnInfo implements Serializable {
	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	/** 保险证号 **/
	private String insuranceCertNo;
	/** 退货物流公司 **/
	private String logisticsCompany;
	/** 退货运单号 **/
	private String logisticsBillNo;
	/** 收款人开户行代码 **/
	private String payeeBankCode;
	/** 收款人开户行名称 **/
	private String payeeBankName;
	/** 收款人 **/
	private String payeeAccName;
	/** 收款人银行账号 **/
	private String payeeBankAccount;
	/** 收款人手机号 **/
	private String payeeMobilePhone;

	public String getInsuranceCertNo() {
		return insuranceCertNo;
	}

	public void setInsuranceCertNo(String insuranceCertNo) {
		this.insuranceCertNo = insuranceCertNo;
	}

	public String getLogisticsCompany() {
		return logisticsCompany;
	}

	public void setLogisticsCompany(String logisticsCompany) {
		this.logisticsCompany = logisticsCompany;
	}

	public String getLogisticsBillNo() {
		return logisticsBillNo;
	}

	public void setLogisticsBillNo(String logisticsBillNo) {
		this.logisticsBillNo = logisticsBillNo;
	}

	public String getPayeeBankCode() {
		return payeeBankCode;
	}

	public void setPayeeBankCode(String payeeBankCode) {
		this.payeeBankCode = payeeBankCode;
	}

	public String getPayeeBankName() {
		return payeeBankName;
	}

	public void setPayeeBankName(String payeeBankName) {
		this.payeeBankName = payeeBankName;
	}

	public String getPayeeAccName() {
		return payeeAccName;
	}

	public void setPayeeAccName(String payeeAccName) {
		this.payeeAccName = payeeAccName;
	}

	public String getPayeeBankAccount() {
		return payeeBankAccount;
	}

	public void setPayeeBankAccount(String payeeBankAccount) {
		this.payeeBankAccount = payeeBankAccount;
	}

	public String getPayeeMobilePhone() {
		return payeeMobilePhone;
	}

	public void setPayeeMobilePhone(String payeeMobilePhone) {
		this.payeeMobilePhone = payeeMobilePhone;
	}

}
