package com.cifpay.insurance.param.policy;

import java.io.Serializable;

/**
 * 导出保单信息结果dto
 * @author yanthe
 *
 */
@SuppressWarnings("serial")
public class GetPolicyExportResult implements Serializable {
	/** 保单号 **/
	private String policyNo;
	/** 投保人 **/
	private String holderName;
	/** 证件号码 **/
	private String idNo;
	/** 预缴保费  ￥33,500.00 **/
	private String initPremium;
	/** 投保日期  yyyy-MM-dd HH:mm:ss **/
	private String insureDate;
	public String getPolicyNo() {
		return policyNo;
	}
	public void setPolicyNo(String policyNo) {
		this.policyNo = policyNo;
	}
	public String getHolderName() {
		return holderName;
	}
	public void setHolderName(String holderName) {
		this.holderName = holderName;
	}
	public String getIdNo() {
		return idNo;
	}
	public void setIdNo(String idNo) {
		this.idNo = idNo;
	}
	public String getInitPremium() {
		return initPremium;
	}
	public void setInitPremium(String initPremium) {
		this.initPremium = initPremium;
	}
	public String getInsureDate() {
		return insureDate;
	}
	public void setInsureDate(String insureDate) {
		this.insureDate = insureDate;
	}
}
