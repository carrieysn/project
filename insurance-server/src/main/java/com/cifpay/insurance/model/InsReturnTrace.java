package com.cifpay.insurance.model;

import java.io.Serializable;
import java.util.Date;

public class InsReturnTrace implements Serializable {

	private static final long serialVersionUID = 19700101000000000L;
	/** id **/
	private Long id;
	/** 保险证ID **/
	private Long insuranceCertId;
	/** 保险证号 **/
	private String insuranceCertNo;
	/** 操作类型 **/
	private String optType;
	/** 跟踪描述 **/
	private String description;
	/** 操作时间 **/
	private Date optTime;
	/** 操作时间毫秒 **/
	private Integer optMicrosecond;

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public Long getInsuranceCertId() {
		return insuranceCertId;
	}

	public void setInsuranceCertId(Long insuranceCertId) {
		this.insuranceCertId = insuranceCertId;
	}

	public String getInsuranceCertNo() {
		return insuranceCertNo;
	}

	public void setInsuranceCertNo(String insuranceCertNo) {
		this.insuranceCertNo = insuranceCertNo;
	}

	public String getOptType() {
		return optType;
	}

	public void setOptType(String optType) {
		this.optType = optType;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}

	public Date getOptTime() {
		return optTime;
	}

	public void setOptTime(Date optTime) {
		this.optTime = optTime;
	}

	public Integer getOptMicrosecond() {
		return optMicrosecond;
	}

	public void setOptMicrosecond(Integer optMicrosecond) {
		this.optMicrosecond = optMicrosecond;
	}

}
