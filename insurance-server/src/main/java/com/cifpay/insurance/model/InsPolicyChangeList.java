package com.cifpay.insurance.model;

import java.io.Serializable;
import java.util.Date;

public class InsPolicyChangeList implements Serializable {

	private static final long serialVersionUID = 19700101000000000L;
	/** id **/
	private Long id;
	/** 商户号 **/
	private String vendorId;
	/** 引用ID **/
	private Long refId;
	/** 引用凭证号 **/
	private String refVoucherNo;
	/** 类型（1-生成；2-退款；3-失效；4-充值） **/
	private Integer type;
	/** 变动金额（分） **/
	private Long changeAmount;
	/** 变动时间 **/
	private Date changeTime;
	/** 变动前保费（分） **/
	private Long beforePremium;
	/** 变动后保费（分） **/
	private Long curPremium;
	/** 变动前保额（分） **/
	private Long beforeInsuredAmount;
	/** 变动后保额（分） **/
	private Long curInsuredAmount;

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getVendorId() {
		return vendorId;
	}

	public void setVendorId(String vendorId) {
		this.vendorId = vendorId;
	}

	public Long getRefId() {
		return refId;
	}

	public void setRefId(Long refId) {
		this.refId = refId;
	}

	public String getRefVoucherNo() {
		return refVoucherNo;
	}

	public void setRefVoucherNo(String refVoucherNo) {
		this.refVoucherNo = refVoucherNo;
	}

	public Integer getType() {
		return type;
	}

	public void setType(Integer type) {
		this.type = type;
	}

	public Long getChangeAmount() {
		return changeAmount;
	}

	public void setChangeAmount(Long changeAmount) {
		this.changeAmount = changeAmount;
	}

	public Date getChangeTime() {
		return changeTime;
	}

	public void setChangeTime(Date changeTime) {
		this.changeTime = changeTime;
	}

	public Long getBeforePremium() {
		return beforePremium;
	}

	public void setBeforePremium(Long beforePremium) {
		this.beforePremium = beforePremium;
	}

	public Long getCurPremium() {
		return curPremium;
	}

	public void setCurPremium(Long curPremium) {
		this.curPremium = curPremium;
	}

	public Long getBeforeInsuredAmount() {
		return beforeInsuredAmount;
	}

	public void setBeforeInsuredAmount(Long beforeInsuredAmount) {
		this.beforeInsuredAmount = beforeInsuredAmount;
	}

	public Long getCurInsuredAmount() {
		return curInsuredAmount;
	}

	public void setCurInsuredAmount(Long curInsuredAmount) {
		this.curInsuredAmount = curInsuredAmount;
	}

}
