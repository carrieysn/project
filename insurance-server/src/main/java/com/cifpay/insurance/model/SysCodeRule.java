package com.cifpay.insurance.model;

import java.io.Serializable;
import java.util.ArrayList;
import java.util.List;

public class SysCodeRule implements Serializable {

	private static final long serialVersionUID = 19700101000000000L;
	private Long id;
	/** 规则名称 **/
	private String codeName;
	/** 当前值 **/
	private String curValue;
	/** 版本 **/
	private Long version;
	/**控制单元 **/
	private String cu;

	private List<SysCodeRuleDt> sysCodeRuleDt = new ArrayList<SysCodeRuleDt>();

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getCodeName() {
		return codeName;
	}

	public void setCodeName(String codeName) {
		this.codeName = codeName;
	}

	public String getCurValue() {
		return curValue;
	}

	public void setCurValue(String curValue) {
		this.curValue = curValue;
	}

	public Long getVersion() {
		return version;
	}

	public void setVersion(Long version) {
		this.version = version;
	}

	public List<SysCodeRuleDt> getSysCodeRuleDt() {
		return sysCodeRuleDt;
	}

	public void setSysCodeRuleDt(List<SysCodeRuleDt> sysCodeRuleDt) {
		this.sysCodeRuleDt = sysCodeRuleDt;
	}

	public String getCu() {
		return cu;
	}

	public void setCu(String cu) {
		this.cu = cu;
	}

}
