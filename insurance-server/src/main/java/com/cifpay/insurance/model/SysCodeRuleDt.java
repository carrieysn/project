package com.cifpay.insurance.model;

import java.io.Serializable;

public class SysCodeRuleDt implements Serializable {

	private static final long serialVersionUID = 19700101000000000L;
	/** id **/
	private Long id;
	/** 规则表Id **/
	private Long codeRuleId;
	/** 规则 **/
	private String rule;
	/** 规则类型（0-变量；1-时间；2-26字母组合；3-常量；4-流水号） **/
	private Integer ruleType;
	/** 排序 **/
	private Integer sort;

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public Long getCodeRuleId() {
		return codeRuleId;
	}

	public void setCodeRuleId(Long codeRuleId) {
		this.codeRuleId = codeRuleId;
	}

	public String getRule() {
		return rule;
	}

	public void setRule(String rule) {
		this.rule = rule;
	}

	public Integer getRuleType() {
		return ruleType;
	}

	public void setRuleType(Integer ruleType) {
		this.ruleType = ruleType;
	}

	public Integer getSort() {
		return sort;
	}

	public void setSort(Integer sort) {
		this.sort = sort;
	}

}
