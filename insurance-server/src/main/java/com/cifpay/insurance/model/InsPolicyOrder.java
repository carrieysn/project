package com.cifpay.insurance.model;

import java.io.Serializable;
import java.util.Date;

public class InsPolicyOrder implements Serializable {

	private static final long serialVersionUID = 19700101000000000L;
	private Long id;
	/** 商户号 **/
	private String vendorId;
	/** 订单号 **/
	private String billNo;
	/** 订单类型(0-投保；1-充值) **/
	private Integer type;
	/** 订单状态（0-未支付；1-已支付） **/
	private Integer status;
	/** 金额（分） **/
	private Long amount;
	/** 银信证流水号 **/
	private String lcId;
	/** 银信证编号 **/
	private String lcNo;
	/** 银信证状态 **/
	private String lcState;
	/** 银行交易时间 **/
	private Date tradeDate;
	/** 充值前保费（分） **/
	private Long beforePremium;
	/** 保单ID **/
	private Long policyId;
	/** 创建人 **/
	private String createdUser;
	/** 创建时间 **/
	private Date createdTime;
	/** 修改人 **/
	private String modifiedUser;
	/** 修改时间 **/
	private Date modifiedTime;
	/** 版本号 **/
	private Integer version;

	private InsPolicyOrderItem insPolicyOrderItem;

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getVendorId() {
		return vendorId;
	}

	public void setVendorId(String vendorId) {
		this.vendorId = vendorId;
	}

	public String getBillNo() {
		return billNo;
	}

	public void setBillNo(String billNo) {
		this.billNo = billNo;
	}

	public Integer getType() {
		return type;
	}

	public void setType(Integer type) {
		this.type = type;
	}

	public Integer getStatus() {
		return status;
	}

	public void setStatus(Integer status) {
		this.status = status;
	}

	public Long getAmount() {
		return amount;
	}

	public void setAmount(Long amount) {
		this.amount = amount;
	}

	public String getLcId() {
		return lcId;
	}

	public void setLcId(String lcId) {
		this.lcId = lcId;
	}

	public String getLcNo() {
		return lcNo;
	}

	public void setLcNo(String lcNo) {
		this.lcNo = lcNo;
	}

	public String getLcState() {
		return lcState;
	}

	public void setLcState(String lcState) {
		this.lcState = lcState;
	}

	public Date getTradeDate() {
		return tradeDate;
	}

	public void setTradeDate(Date tradeDate) {
		this.tradeDate = tradeDate;
	}

	public Long getBeforePremium() {
		return beforePremium;
	}

	public void setBeforePremium(Long beforePremium) {
		this.beforePremium = beforePremium;
	}

	public Long getPolicyId() {
		return policyId;
	}

	public void setPolicyId(Long policyId) {
		this.policyId = policyId;
	}

	public String getCreatedUser() {
		return createdUser;
	}

	public void setCreatedUser(String createdUser) {
		this.createdUser = createdUser;
	}

	public Date getCreatedTime() {
		return createdTime;
	}

	public void setCreatedTime(Date createdTime) {
		this.createdTime = createdTime;
	}

	public String getModifiedUser() {
		return modifiedUser;
	}

	public void setModifiedUser(String modifiedUser) {
		this.modifiedUser = modifiedUser;
	}

	public Date getModifiedTime() {
		return modifiedTime;
	}

	public void setModifiedTime(Date modifiedTime) {
		this.modifiedTime = modifiedTime;
	}

	public Integer getVersion() {
		return version;
	}

	public void setVersion(Integer version) {
		this.version = version;
	}

	public InsPolicyOrderItem getInsPolicyOrderItem() {
		return insPolicyOrderItem;
	}

	public void setInsPolicyOrderItem(InsPolicyOrderItem insPolicyOrderItem) {
		this.insPolicyOrderItem = insPolicyOrderItem;
	}

}
